    $(document).ready(function(){
        // $.ajaxSetup({ cache: false });
        $('#search_secciones').keyup(function(){

            console.log("🚀 ~ file: footer.php:221 ~ $ ~   $(this):",   $(this))
            $('#result_busqueda_seccion').html('');
            $('#state').val('');
            var searchField = $('#search_secciones').val();
            var expression = new RegExp(searchField, "i");
            var parametros_ini = {
                            "id": 0,
                        };
                    $.ajax({
                    type  : "POST",
                    url   : '<?php echo base_url('index.php?admin/usuarios/Inicioadmin/Buscador_secciones'); ?>',
                    data  : parametros_ini, 
                    })
                    .done(function(data) {
                        var data = JSON.parse(data);
 			$('#result_busqueda_seccion').html('');
                        $.each(data, function(key, value){
                            if (value.name.search(expression) != -1 || value.icon.search(expression) != -1)
                            {
                                $('#result_busqueda_seccion').append(
                                `
                                    <li class="list-group-item link-class-busqueda" onClick="buscar_seccion('${value.url}')">
                                            <span class="text-muted">${value.icon}</span> | ${value.name} 
                                    </li>
                                `);
                            }
                        });   
                    }).fail(function(jqXHR, textStatus, errorThrown) {someErrorFunction();}).always(function() {});
        });
        
    });

    

